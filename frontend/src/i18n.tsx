interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, params?: Record<string, string | number | null | undefined>) => string;
  display: (kind: string, value?: string | null) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function detectInitialLocale(): Locale {
  const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (saved === "zh-CN" || saved === "en-US") return saved;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en-US";
}

function interpolate(template: string, params?: Record<string, string | number | null | undefined>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ""));
}

function humanize(value?: string | null): string {
  if (!value) return "";
  return value
    .replace(/_/g, " ")
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => detectInitialLocale());

  const setLocale = (next: Locale) => {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, next);
    setLocaleState(next);
  };

  useEffect(() => {
    document.documentElement.lang = locale === "zh-CN" ? "zh-CN" : "en";
  }, [locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, params) => interpolate((locale === "zh-CN" ? zh : en)[key] ?? zh[key] ?? key, params),
      display: (kind, rawValue) => {
        const value = rawValue ?? "";
        return displayLabels[locale][kind]?.[value] ?? displayLabels["zh-CN"][kind]?.[value] ?? humanize(value);
      }
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used within I18nProvider");
  return value;
}

export function LanguageToggle({ compact = false }: { compact?: boolean }) {
  const { locale, setLocale, t } = useI18n();
  const next = locale === "zh-CN" ? "en-US" : "zh-CN";
  return (
    <button
      className={compact ? "btn h-9 w-9 px-0" : "btn gap-1"}
      type="button"
      onClick={() => setLocale(next)}
      title={t("language.toggle")}
      aria-label={t("language.toggle")}
    >
      <Languages size={16} />
      {!compact && <span>{locale === "zh-CN" ? "中文" : "EN"}</span>}
    </button>
  );
}
